def deobfuscate(obfuscated, key):
    decrypted = bytearray()
    for i in range(len(obfuscated)):
        key_index = i + ((i - i // 7 >> 1) + i // 7 >> 2) * -7  # Reverse the key index calculation
        decrypted.append(obfuscated[i] ^ key[key_index % len(key)])
    return decrypted.decode()

